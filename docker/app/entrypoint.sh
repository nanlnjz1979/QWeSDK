#!/bin/bash
# QWeSDK Docker 入口点脚本
# 处理版本检测和安装，然后执行用户指定的命令

set -e

echo "=========================================="
echo "QWeSDK Docker 启动脚本"
echo "=========================================="

# 运行版本检测和安装脚本
python3 /app/qwesdk_entrypoint.py

